public class plusemethod{
     static int plusemethodint(int x,int y){
        return x+y;
    }
    static double plusemethoddouble(double x, double y){
        return x+y;
    }
    public static void main(String[] args) {
        System.out.println(plusemethodint(4,6));
        System.out.println(plusemethoddouble(23.2, 52.3));
    }
    
}